//.reverse() --> a method for reversing elements in the array:

var reverseMe = ["h", "e", "l", "l", "o"];
console.log(reverseMe.reverse());

//.sort() is used to reverse elements in an array:

var sortMe = [2, 1, 3, 4, 7, 5, 6];
console.log(sortMe.sort());

//.shift()  --> for removing the first element in array:
var removeFirstElement = ["a", "b", "c"];
console.log(removeFirstElement.shift());

//join('') --> to join an array into a string
var turnMeIntoAString = ["U", "d", "a", "c", "i", "t", "y"];
console.log(turnMeIntoAString.join(''));
